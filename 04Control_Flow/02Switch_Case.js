// =============== In Case of String ==============
const month = "fab"

switch (month) {
    case "jan":
        console.log("january");
        
        break;
    case "fab":
        console.log("Fab");
        
        break;
    case "march":
        console.log("Mar");
        
        break;
    case "April":
        console.log("Apr");
        
        break;

    default:
        console.log("default case match");
        
        break;
}

