//  =============  Switch Case Statement  ================

const month = 3

switch (month) {
    case 1:
        console.log("january");
        
        break;
    case 2:
        console.log("Fab");
        
        break;
    case 3:
        console.log("Mar");
        
        break;
    case 4:
        console.log("Apr");
        
        break;

    default:
        console.log("default case match");
        
        break;
}